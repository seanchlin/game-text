    frequency = {w: specific_words_count[w] / length for w in specific_words_count}
    standarized_frequency = {w: frequency[w] * weight[file_num - 1] for w in frequency}
    total_standarized_frequency.append(sorted(standarized_frequency.items(), key=lambda x: x[1], reverse=True))

    for word in result:
        if word not in file_times:
            file_times[word] = 1
        if file_times[word] < file_num:
            file_times[word] += 1

