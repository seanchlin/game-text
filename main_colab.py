def find_highest_value(category):
    if category not in classification:
        print(f"分類 '{category}' 不存在。")
        return
    terms = classification[category]
    if not terms:
        print(f"分類 '{category}' 沒有對應的名詞。")
        return
    max_term = None
    max_value = float('-inf')
    for term in terms:
        if term in tf_idf and tf_idf[term] > max_value:
            max_term = term
            max_value = tf_idf[term]
    if max_term is not None:
        print(f"分類 '{category}' 中數值最高的名詞是：'{max_term}'，數值為：{max_value}")
        print("---------------------------------------------")
        print(f"推薦{category}為: {max_term}")
    else:
        print(f"分類 '{category}' 中沒有匹配的名詞。")

